function t(key) {
  const lang = localStorage.getItem('cvita_language') || 'sv';
  const translations = TRANSLATIONS[lang] || TRANSLATIONS['sv'];
  return translations[key] || TRANSLATIONS['sv'][key] || key;
}

function applyTranslations() {
  const lang = localStorage.getItem('cvita_language') || 'sv';
  
  // data-i18n attribute'u olan elementleri çevir
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const text = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = text;
    } else {
      el.textContent = text;
    }
  });

  // data-i18n-placeholder olan inputları çevir
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = t(key);
  });

  // İsveççe değilse direkt metin replacement yap
  if (lang !== 'sv') {
    const map = TEXT_MAP[lang];
    if (!map) return;
    
    // Text node'larını dolaş ve değiştir
    function walkTextNodes(node) {
      if (node.nodeType === 3) { // Text node
        const parent = node.parentNode;
        if (!parent) return;
        const tag = parent.tagName;
        // Script ve style taglarını atla
        if (tag === 'SCRIPT' || tag === 'STYLE') return;
        
        let text = node.textContent;
        let changed = false;
        for (const [sv, translated] of Object.entries(map)) {
          if (text.includes(sv)) {
            text = text.replace(new RegExp(sv.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g'), translated);
            changed = true;
          }
        }
        if (changed) node.textContent = text;
      } else {
        node.childNodes.forEach(walkTextNodes);
      }
    }
    
    walkTextNodes(document.body);
  }
}

function setLanguage(lang) {
  localStorage.setItem('cvita_language', lang);
  applyTranslations();
}
